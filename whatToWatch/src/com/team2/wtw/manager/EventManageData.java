public class EventManageData {
	
	   private int eventNo;
	    private int memberNo;
	    private int event1Score;
	    private int event2Score;

	    public int getEventNo() {
	        return eventNo;
	    }

	    public void setEventNo(int eventNo) {
	        this.eventNo = eventNo;
	    }

	    public int getMemberNo() {
	        return memberNo;
	    }

	    public void setMemberNo(int memberNo) {
	        this.memberNo = memberNo;
	    }

	    public int getEvent1Score() {
	        return event1Score;
	    }

	    public void setEvent1Score(int event1Score) {
	        this.event1Score = event1Score;
	    }

	    public int getEvent2Score() {
	        return event2Score;
	    }

	    public void setEvent2Score(int event2Score) {
	        this.event2Score = event2Score;
	    }
	

}
